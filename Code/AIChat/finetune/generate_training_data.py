# ================= Main =================

def main():
    """Generate all training data and save."""
    print("Generating training data...")

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    all_data = []

    # Generate each category
    print("Generating student coach data...")
    student_data = generate_student_coach_data()
    all_data.extend(student_data)
    print(f"  - Generated {len(student_data)} samples")

    print("Generating teacher assistant data...")
    teacher_data = generate_teacher_assistant_data()
    all_data.extend(teacher_data)
    print(f"  - Generated {len(teacher_data)} samples")

    print("Generating contextual analysis data...")
    context_data = generate_contextual_analysis_data()
    all_data.extend(context_data)
    print(f"  - Generated {len(context_data)} samples")

    print("Generating multi-turn conversation data...")
    multi_turn_data = generate_multi_turn_data()
    all_data.extend(multi_turn_data)
    print(f"  - Generated {len(multi_turn_data)} samples")

    # Shuffle
    random.shuffle(all_data)

    # Save
    output_file = os.path.join(OUTPUT_DIR, "training_data.jsonl")
    with open(output_file, "w", encoding="utf-8") as f:
        for item in all_data:
            f.write(json.dumps(item, ensure_ascii=False) + "\n")

    print(f"\n{'='*50}")
    print("Training data generation complete!")
    print(f"{'='*50}")
    print(f"Total samples: {len(all_data)}")
    print(f"Saved to: {output_file}")
    print(f"\nBreakdown:")
    print(f"  - Student coach: {len(student_data)}")
    print(f"  - Teacher assistant: {len(teacher_data)}")
    print(f"  - Contextual analysis: {len(context_data)}")
    print(f"  - Multi-turn: {len(multi_turn_data)}")

    # Estimate training time
    # For 8GB VRAM: batch_size=1, grad_accum=8, ~500 samples
    # ~2-3 hours for 500 samples, 3 epochs
    print(f"\nEstimated training time (8GB VRAM):")
    print(f"  - {len(all_data)} samples × 3 epochs")
    print(f"  - Approximately {len(all_data) * 3 // 100 + 1} hours")


if __name__ == "__main__":
    main()
