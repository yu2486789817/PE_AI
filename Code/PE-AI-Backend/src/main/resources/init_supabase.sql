-- PE AI 智慧运动课堂 - Supabase PostgreSQL 初始化脚本
-- 在 Supabase Dashboard -> SQL Editor 中执行。

DROP TABLE IF EXISTS submit CASCADE;
DROP TABLE IF EXISTS ai_type CASCADE;
DROP TABLE IF EXISTS homework CASCADE;
DROP TABLE IF EXISTS student_course CASCADE;
DROP TABLE IF EXISTS class CASCADE;
DROP TABLE IF EXISTS course CASCADE;
DROP TABLE IF EXISTS student CASCADE;
DROP TABLE IF EXISTS teacher CASCADE;
DROP TABLE IF EXISTS std_student CASCADE;
DROP TABLE IF EXISTS std_teacher CASCADE;

CREATE TABLE std_teacher (
    id varchar(20) PRIMARY KEY,
    name varchar(50) NOT NULL
);

CREATE TABLE std_student (
    id varchar(20) PRIMARY KEY,
    name varchar(50) NOT NULL
);

CREATE TABLE teacher (
    id varchar(20) PRIMARY KEY,
    password varchar(64) NOT NULL,
    name varchar(50) NOT NULL,
    gender varchar(10),
    title varchar(50),
    college varchar(100),
    department varchar(100),
    login_time timestamp,
    created_time timestamp DEFAULT current_timestamp
);

CREATE TABLE student (
    id varchar(20) PRIMARY KEY,
    password varchar(64) NOT NULL,
    name varchar(50) NOT NULL,
    gender varchar(10),
    college varchar(100),
    department varchar(100),
    major varchar(100),
    login_time timestamp,
    created_time timestamp DEFAULT current_timestamp
);

CREATE TABLE course (
    id varchar(20) PRIMARY KEY,
    teacher_id varchar(20) NOT NULL,
    name varchar(100) NOT NULL,
    info text,
    code varchar(20) NOT NULL UNIQUE,
    semester integer DEFAULT 1,
    is_active smallint DEFAULT 1,
    created_time timestamp DEFAULT current_timestamp
);

CREATE TABLE student_course (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id varchar(20) NOT NULL,
    course_id varchar(20) NOT NULL,
    joined_time timestamp DEFAULT current_timestamp,
    CONSTRAINT uk_student_course UNIQUE (student_id, course_id)
);

CREATE TABLE homework (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_id varchar(20) NOT NULL,
    title varchar(200) NOT NULL,
    description text,
    deadline timestamp,
    create_time timestamp DEFAULT current_timestamp
);

CREATE TABLE ai_type (
    homework_id integer PRIMARY KEY,
    type varchar(50),
    num integer DEFAULT 0
);

CREATE TABLE submit (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    homework_id integer NOT NULL,
    student_id varchar(20) NOT NULL,
    video_url varchar(500),
    score integer,
    ai_feedback text,
    teacher_feedback text,
    create_time timestamp DEFAULT current_timestamp
);

CREATE TABLE class (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    course_id varchar(20) NOT NULL,
    title varchar(200) NOT NULL,
    description text,
    content_url varchar(500),
    create_time timestamp DEFAULT current_timestamp
);

